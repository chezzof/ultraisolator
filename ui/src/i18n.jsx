const I18nContext = createContext({
  language: 'en',
  t: (_key, fallback) => fallback
});

function normalizeLanguage(language) {
  return language === 'ru' ? 'ru' : 'en';
}

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState('en');

  useEffect(() => {
    let cancelled = false;
    loadAppSettings().then((settings) => {
      if (!cancelled) {
        setLanguage(normalizeLanguage(settings.language));
      }
    }).catch(() => {});
    const onSettingsUpdated = (event) => {
      setLanguage(normalizeLanguage(event.detail?.language));
    };
    window.addEventListener('app-settings-updated', onSettingsUpdated);
    return () => {
      cancelled = true;
      window.removeEventListener('app-settings-updated', onSettingsUpdated);
    };
  }, []);

  const value = useMemo(() => ({
    language,
    t: (key, fallback = key) => MESSAGES[language]?.[key] || MESSAGES.en[key] || fallback
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
